package Model;

public class Role {
	private int id;
    private String roleName;

    // Getter và Setter cho các trường
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public String getRoleName() {
        return roleName;
    }
    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }
}
